function main() {
  const data = JSON.parse(fs.readFileSync(FILE, 'utf8'));
  const byId = new Map(data.map((e) => [e.id, e]));

  let appended = 0;
  let skipped = 0;
  for (const entry of [...HATCHES, ...NON_HATCH]) {
    if (byId.has(entry.id)) {
      skipped++;
      continue;
    }
    // Default optional fields
    if (entry.searchTerm == null) entry.searchTerm = entry.name;
    if (entry.wikipediaSlug === undefined) entry.wikipediaSlug = null;
    data.push(entry);
    byId.set(entry.id, entry);
    appended++;
  }

  fs.writeFileSync(FILE, JSON.stringify(data, null, 2) + '\n', 'utf8');
  console.log(`Appended ${appended} new hatch/fly entries, skipped ${skipped}. Total: ${data.length}`);

  // Per-state fly-pattern counts
  const STATE_FLIES = {};
  for (const entry of data) {
    const states = entry.states && entry.states.length ? entry.states : [];
    const flyCount = (entry.flies || []).length;
    for (const s of states) {
      STATE_FLIES[s] = (STATE_FLIES[s] || 0) + flyCount;
    }
  }
  const sorted = Object.entries(STATE_FLIES).sort((a, b) => b[1] - a[1]);
  console.log('\nFly patterns per state (top 30):');
  for (const [s, c] of sorted.slice(0, 30)) console.log(`  ${s}: ${c}`);
}

main();
